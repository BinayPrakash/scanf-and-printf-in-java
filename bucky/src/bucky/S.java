public class S {
	public static String scanS()
	{
		Scanner p=new Scanner(System.in);
				String a=p.next();
		return a;
	}
	public static int scanI()
	{
		Scanner p=new Scanner(System.in);
				int b=p.nextInt();
		return b;
	}
	public static float scanF()
	{
		Scanner p=new Scanner(System.in);
				float c=p.nextFloat();
		return c;
	}
	public static void printf(String x)
	{
		System.out.println(x);
		
		}

		public static void printf(int a)
		{
		System.out.println(a);
		}

		public static void printf(float a)
		{
		System.out.println(a);
		}
	
}